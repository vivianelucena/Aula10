'''
classe - molde de algo
    1. atributos (criar através de variáveis)
    2. métodos -> comportamento - o que dá a ação (funções -> def)

objeto - é uma instância de uma classe
    geralmente através de uma API
    API -> faz a interface (comunica entre dois sistemas para a troca de mensagens)


Pilares:
    1. abstração -> resultado da mensagem
    2. encapsulamento -> proteger (apenas métodos que pertecem a classe pode vê-lo)
    3. herança -> herdar características, (classe filho, ou subclasse) recebe atributos e métodos da classe mãe (super classe ou classe pai)
                capacidade de herdar atributos e métodos de uma classe nessa nova classe
    4. polimorfismo -> atributos e métodos herdados, mas de forma diferente


Classes > iniciam com letra maiúscula - padrão Pascal case -> PascalCase

Métodos > iniciam com letra minúscula, mas a segunda palavra inicia com letra maiúscula - padrão Camel case -> camelCase

self -> associa o atributo a variável
'''